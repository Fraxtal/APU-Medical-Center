    public void loadUserData(String username, String fullname, String email, String address, String contact, String password, String role, String creationDate) {
        tbUsername.setText(username);
        tbFullname.setText(fullname);
        tbEmail.setText(email);
        tbAddress.setText(address);
        tbContact.setText(contact);
        tbPassword.setText(password);
        tbRole.setText(role);
        tbDate.setText(creationDate);
    }

    private void updateUserProfile() {
        try {
            String username = tbUsername.getText().trim();
            String fullname = tbFullname.getText().trim();
            String email = tbEmail.getText().trim();
            String address = tbAddress.getText().trim();
            String contact = tbContact.getText().trim();
            String password = tbPassword.getText().trim();

            if (username.isEmpty() || fullname.isEmpty() || email.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Username, Full Name, and Email are required fields.", "Validation Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (currentUser != null) {
                boolean success = currentUser.updateProfile(username, fullname, email, address, contact, password);
                if (success) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Profile updated successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    if (navigationCallback != null) {
                        this.dispose();
                        navigationCallback.onBack();
                    }
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Failed to update profile. Please try again.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No user logged in.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            logger.severe("Error updating user profile: " + e.getMessage());
            javax.swing.JOptionPane.showMessageDialog(this, "An error occurred while updating the profile.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

