    private void loadDoctors() {
        if (customerService != null) {
            List<String> doctors = customerService.getDoctors();
            cmbDoctor.removeAllItems();
            for (String doctor : doctors) {
                cmbDoctor.addItem(doctor);
            }
        }
    }

