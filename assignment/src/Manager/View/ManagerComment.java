    private void DisplayDetail()
    {
        tableModelDetail.setColumnCount(1);
        String[] col = controller.GetUserDetail(jTable2.getSelectedRow());
        if (col != null && col.length > 0)
        {
            tableModelDetail.addColumn("Data", col);
        }
    }
    
    @Override
    public void ShowErrorDialog(String message) {
        JOptionPane.showMessageDialog(this, message, "Error", JOptionPane.ERROR_MESSAGE);
    }

    @Override
    public void InitializeTableModel(String[] columnName) {
        tableModel.setColumnIdentifiers(columnName);
    }
    
    @Override
    public void LoadDisplay(List<String[]> content) {
        tableModel.setRowCount(0);
        for (String[] row : content)
        {
            tableModel.addRow(row);
        }
    }

    @Override
    public List<String> GetTableRow(int index) {
        List<String> rowValue = new ArrayList<>();
        for (int i = 0; i < jTable2.getColumnCount(); i++)
        {
            Object value = jTable2.getValueAt(index, i);
            rowValue.add(value == null ? "" : value.toString());
        }
        return rowValue;
    }
